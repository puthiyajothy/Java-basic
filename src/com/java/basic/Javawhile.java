package com.java.basic;

public class Javawhile {

	public static void main(String[] args) {
		
		int i=1;
		
		
		while(i<=5) {
			System.out.println("hellow");
			
			i++;
		}
		
		System.out.println();
		
		
		for(i=1;i<=5;i++) {
			System.out.println("hellow");
		}
		
		System.out.println();
		
	}

}
