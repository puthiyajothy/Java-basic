package com.java.basic;

public class JavaStar {

	public static void main(String[] args) {
		
		int i;
		int j;
		 
		
		for (i=8;i>=1;i--) {
			for(j=1;j<i;j++) {
				
				System.out.print("*");
			}
			
			System.out.println(" ");
			
		}
		
		System.out.println();
		
		for(i=1;i<=8;i++) {
			
				System.out.print("*");
			
		}
	}

}
